"""
generate_test_audio.py — Synthesize a ~3-minute Arabic AI lecture for demo testing.

The text is written entirely in MSA Arabic — no English tokens at all. Technical
terms use their established Arabic equivalents (الذكاء الاصطناعي، تعلم الآلة،
الشبكات العصبية، …) so Whisper runs on a clean Arabic-only target rather than
fighting code-switching on the Arabic/Latin boundary.

Install once:
    pip install edge-tts

Run:
    python generate_test_audio.py

Output:
    arabic_lecture_sample.mp3   (~3 min, MSA, neural male voice)
"""

import asyncio
import edge_tts


# ~500 words of pure MSA — no Latin characters.
LECTURE = """
السلام عليكم ورحمة الله وبركاته. أهلًا بكم في محاضرة اليوم. سنتحدث عن مجال
الذكاء الاصطناعي، وكيف يغير حياتنا اليومية بشكل جذري.

الذكاء الاصطناعي ينقسم إلى عدة فروع رئيسية. أولها تعلم الآلة، وهو الفرع
الذي يهتم بتطوير خوارزميات قادرة على التعلم من البيانات دون الحاجة إلى
برمجة صريحة. ثم لدينا التعلم العميق، الذي يعتمد على الشبكات العصبية
الاصطناعية ذات الطبقات المتعددة، ويتطلب عادةً قوة حسابية عالية توفرها
وحدات المعالجة الرسومية.

في التعلم العميق، نستخدم خوارزمية شهيرة تسمى الانتشار العكسي لتدريب
الشبكة. تبدأ الشبكة بأوزان عشوائية، ثم نمرر البيانات عبرها، ونحسب دالة
الخسارة، وبعد ذلك نُحدّث الأوزان باستخدام خوارزمية الانحدار الاشتقاقي.
هذه العملية تتكرر آلاف المرات حتى يصل النموذج إلى أداء جيد.

من أهم التطورات الحديثة في هذا المجال ظهور معمارية المحوّل عام ألفين
وسبعة عشر، والتي قدمتها إحدى الشركات الكبرى في ورقة بحثية شهيرة بعنوان
الانتباه هو كل ما تحتاجه. هذه المعمارية أحدثت ثورة في مجال معالجة اللغة
الطبيعية.

من تطبيقات معمارية المحوّل نماذج لغوية ضخمة متعددة، تختلف في الحجم
والمهام التي تتقنها. وفي مجال اللغة العربية، لدينا نماذج متخصصة مدربة
على مليارات الكلمات العربية. وأيضًا في مجال التعرف على الكلام، يوجد
نموذج مفتوح المصدر يُعتبر من أقوى النماذج المتاحة، ويدعم اللغة العربية
بكفاءة عالية.

دعونا نتحدث عن خط المعالجة الذي بُني عليه هذا المشروع. نبدأ بمرحلة
تحويل الكلام إلى نص، حيث نستخدم نموذج التعرف على الكلام لتحويل الصوت
إلى نص مكتوب. ثم تأتي مرحلة التضمين، حيث نستخدم نموذجًا عربيًا متخصصًا
لتمثيل النص كمتجهات عددية. بعد ذلك نبني فهرس بحث دلالي باستخدام مكتبة
سريعة طورتها إحدى الشركات الكبرى لإجراء البحث الدلالي بكفاءة عالية.

أخيرًا، في مرحلة التلخيص، نستخدم نموذجًا عربيًا قمنا بضبطه على مجموعة
بيانات تلخيص عربية واسعة. النتائج كانت ممتازة، حيث حصلنا على أداء
يضاهي أحدث النماذج المنشورة في الأبحاث العلمية.

لكن مع كل هذه الإمكانيات، هناك تحديات حقيقية. أولها قضية الهلوسة، حيث
قد يولد النموذج معلومات غير صحيحة بثقة عالية. ثانيًا، قضية التحيز، الذي
قد ينتقل من بيانات التدريب إلى النموذج. وثالثًا، التكلفة الحاسوبية
العالية، فتدريب نموذج لغوي كبير قد يحتاج إلى مئات وحدات المعالجة
الرسومية ويستغرق أسابيع.

في الختام، الذكاء الاصطناعي ثورة حقيقية ستعيد تشكيل العالم في السنوات
القادمة. من المهم جدًا أن نواكب هذه التطورات، وأن نتعلم استخدام أحدث
الأدوات البرمجية لبناء حلول عربية تخدم مجتمعاتنا.
شكرًا لكم على حسن الاستماع.
"""

# MSA male voice. Alternatives:
#   ar-EG-ShakirNeural  (Egyptian male, warmer)
#   ar-SY-LaithNeural   (Levantine male)
VOICE = "ar-SA-HamedNeural"
RATE  = "-5%"                 # slightly slower → easier ASR target
OUT   = "arabic_lecture_sample.mp3"


async def main():
    print(f"Synthesizing {len(LECTURE.split())} words with {VOICE} ...")
    communicate = edge_tts.Communicate(LECTURE.strip(), VOICE, rate=RATE)
    await communicate.save(OUT)
    print(f"Done -> {OUT}")


if __name__ == "__main__":
    asyncio.run(main())
